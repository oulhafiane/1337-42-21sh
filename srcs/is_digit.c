#include "shell.h"

int			is_digit(char **tmp, t_duped *duped)
{
	if (**tmp && ft_isdigit(**tmp))
	{
		duped->filed2 = **tmp - '0';
		(*tmp)++;
		return (1);
	}
	return (0);
}

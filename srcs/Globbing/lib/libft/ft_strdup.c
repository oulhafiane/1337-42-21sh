#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*string;
	int		i;

	i = 0;
	string = (char *)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (string == NULL)
		return (NULL);
	while (src[i])
	{
		string[i] = src[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
